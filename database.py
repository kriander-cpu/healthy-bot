def find_product(query: str):
    """
    Ищет продукт в базе по названию.
    Возвращает кортеж (найденное_название, данные) или (None, список_похожих_названий).
    """
    query = query.strip().lower()

    # Точное совпадение
    if query in PRODUCTS:
        return query, PRODUCTS[query]

    # Поиск по вхождению подстроки (например "курица" найдёт "куриная грудка")
    matches = [name for name in PRODUCTS if query in name or name in query]
    if matches:
        # Берём наиболее короткое совпадение как наиболее релевантное
        best = min(matches, key=len)
        return best, PRODUCTS[best]

    # Поиск по совпадению отдельных слов запроса со словами в названии продукта
    # (например, "куриное филе" пересечётся по слову с "куриная грудка" не будет,
    # но частичное вхождение вроде "куриное" -> "куриное бедро" сработает)
    query_words = set(query.split())
    word_matches = []
    for name in PRODUCTS:
        name_words = set(name.split())
        if query_words & name_words:
            word_matches.append(name)
    if word_matches:
        return None, sorted(word_matches, key=len)[:5]

    # Нечёткий поиск (fuzzy matching) через difflib
    close = difflib.get_close_matches(query, PRODUCTS.keys(), n=3, cutoff=0.4)
    if close:
        return None, close

    return None, []


